package week2.day1;

public class LearnSwitch {

	public static void main(String[] args) {
		int day=1;
		
		switch (day) {
		case 1:
			System.out.println("Monday");
			break;
		case 2:
			System.out.println("Tuesday");
			break;
		case 3:
			System.out.println("Thursday");
			break;
		case 4:
			System.out.println("Friday");
			break;

		default:
			System.out.println("invalid number");
			break;
		}
	

	}

}
